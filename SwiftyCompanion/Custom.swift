import SwiftUI

func customHeadline() -> Font {
    return Font.system(size: 17, weight: Font.Weight.bold, design: Font.Design.rounded)
}

func customBody() -> Font {
    return Font.system(Font.TextStyle.body, design: Font.Design.default)
}
